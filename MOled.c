#include "MOled.h"

extern int fd_oled;
extern char oledImage[512];
int negative;

void clearImage()
{
	negative = 0;
	memset(oledImage, 0X0, sizeof(oledImage));
}

void getOledFD()
{
	fd_oled = open("/dev/zed_oled",O_RDWR);
	if(fd_oled == -1) {
		printf("Can't open zed_oled device!\n");
	} 
	else {
		printf("/dev/zed_oled open success!\n");
	}
}

void closeOled()
{
	clearImage();
	printImage();
	close(fd_oled);
}

void printImage()
{
	write(fd_oled, oledImage, 128*4);
}


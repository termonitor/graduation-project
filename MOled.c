void handleNumber(int num)
{
	negative = 0;
	if(num == 0)
    {
        setNum(1, 0);
        return;   
    }      
	if(num < 0)
	{
		negative = 1;
		num = abs(num);
	}
    int i=1; 
    while(num != 0)
    {
        int temp = num%10;
        setNum(i, temp);
        i++;
        num /= 10;
    }
	if(negative)
		addNegative(i);
}